#Python
#some of the math methods
import math
def math_methods():
    #math contants
    print("e:",math.e)   #returns Euler's number
    print("pi:",math.pi)  #returns pi value
    print("tau:",math.tau) #retruns tau value
    #math methods
    print("ceil:",math.ceil(45.4))  #returns number up to the nearest integer
    print("floor:",math.floor(45.4)) #returns number down to the nearest integer
    print("cos:",math.cos(10))     #returns cosine of a number
    print("sin:",math.sin(10))      #returns sine of a number
    print("tan:",math.tan(10))      #returns tangent of a number
    print("radians:",math.radians(57.29577951308232))   #converts degrees to radians
    print("degrees:",math.degrees(1))  #converts radians to degrees
    print("exp:",math.exp(65))     #returns exponential value of a number
    print("factorial:",math.factorial(5))#returns factorial of a number
    print("gcd:",math.gcd(3,6))    #returns greatest common divisor of two numbers
    print("fmod:",math.fmod(20,4))  #returns the remainder of x/y
    print("isclose:",math.isclose(12.344,12.354))   #checks whether two values are close to each other
    print("pow:",math.pow(2,3)) #returns value of x power y of (x,y)
    print("sqrt:",math.sqrt(25))    #returns square root of a given value
    #more and more math methods are given by python
math_methods()
