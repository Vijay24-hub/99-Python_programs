#Python
#Casting program
#int , float , str literals are
#can be casting using constructor funtion
def Casting():
    print("**Int**")
    #int --> int
    #float --> int
    #str --> int
    print(int(10),int(10.0),int("10"))  
    print("**Float**")
    #int --> float
    #float --> float
    #str --> float
    #float str --> float
    print(float(55),float(50.1),float("55"),float("50.1"))
    print("**String**")
    #int --> str
    #float --> str
    #str --> str
    print(str(191),str(191.0),str("191"))
Casting()
