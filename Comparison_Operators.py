#Performing Comparison Operations
def Comparison_Operators(a,b):
    print("a==b:",a==b)
    print("a!=b:",a!=b)
    print("a>b:",a>b)
    print("a<b:",a<b)
    print("a>=b:",a>=b)
    print("a<=b:",a<=b)
a=int(input("Give a value:"))   #a input
b=int(input("Give b value:"))   #b input
Comparison_Operators(a,b)   #Calling a function
    
